function _save(rel) {
	
	cur = find($('test-current-question').value);
	
	if (rel > 0 && !_set(cur.label)) {
		$('errors').innerHTML = '<p>The following error was encountered:</p><ul><li>You have not answered this question.</li></ul><p>Please enter data in the required field and resubmit.</p>';	
		new Effect.Appear('errors',{queue:'end'});
		return;
	} else if (rel < 0) {
		delete cur.score;
	}
	
	if (dbg) console.log("Moving "+ (rel>0?'forward':'backward')+'...');
	
	
	next_q = find(cur.label,rel);
	
	if (!next_q) return;
	
	if (dbg) {
		console.log((rel>0?'Next':'Previous')+" question is "+next_q.label);
		console.debug(next_q);
	}
	if (rel>0) next_q.prev = cur.label;
	render(next_q.label);
}
function _set(label) {
	for (var i=0; i<q.length; i++) {
		if (q[i] && q[i].label	== label) {
			if (dbg) console.log("Q"+ label + ' found...');
			for(var j=0;j<q[i].answers.length;j++) {
				if ($('test-answer-'+q[i].answers[j].value).checked) {
					if (dbg) console.log("Saving score "+ q[i].answers[j].value+" ("+q[i].answers[j].name+") for question "+q[i].label);
					q[i].score = q[i].answers[j].value;
					return true;
				}	
			}
		}
	}
	return false;
}

