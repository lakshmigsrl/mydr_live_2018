function find ( label, rel ) {
	
	if (!rel) rel = 0;
	if (dbg) console.log("Looking for question "+(rel>0?'AFTER ':(rel<0)?'BEFORE ':'')+"#"+label+" ("+rel+")");
	for (var i=0; i<q.length; i++ ) {
		if (dbg) console.log(".. Evaluating question '"+q[i].label+"'"+(q[i].label == label?' (FOUND!)':'')+' (i='+i+')');
		if (q[i].label.toString() == label.toString()) {
			if (rel != 0) {
				
				if (dbg) console.log("... attempting to find "+(rel>0?'NEXT':(rel<0)?'PREVIOUS':'')+" question");
			
				ans = answer(q[i]);
				
				if (rel<0) {
					if (q[i].prev) {
						if (dbg) console.log("... Q."+q[i].prev+" seems to be the one...");
						return find(q[i].prev);
					} /*else if (i>0) { 
						if (dbg) console.log("... a previous one seems to exist... (Q:"+q[i-1].label+")");
						return q[--i];
					}*/
					if (dbg) console.log("... NOT found");
				} else {
					if (ans && ans.next) {
						if (dbg) console.log("... Q."+ans.next+" seems to be the one...");
						return find(ans.next);
					} else if (i<q.length-1) { 
						if (dbg) console.log("... a next one seems to exist... (Q:"+q[i+1].label+" - "+i+")");
						return q[i+1];
					}
					if (dbg) console.log("... NOT found");
				}
				
				return null;					
			}
			
			if (dbg) console.debug(q[i]);
			return q[i];
		}
	}
	return null;
}

function answer(c) {
	if (typeof c.score != 'undefined') {
		if (dbg) console.log("Looking for selected answer for Q."+c.label);
		for(var i=0;i<c.answers.length;i++) {
			if (c.answers[i].value == c.score) {
				if (dbg) console.log(".. FOUND - "+c.answers[i].name+" ("+c.answers[i].value+") was selected");
				return c.answers[i];
			}
		}	
	}
	return null;
}

