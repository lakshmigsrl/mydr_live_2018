function _calc(start, end) {
	var score = 0;
	var count = false;
	
	if (dbg) console.log('Calculating score for Q'+start+' to '+end);
	
	for(var i=0;i<q.length;i++) {
		
	
		if (q[i].label == start) count= true;
		
		if (count && q[i].score > 0) score += q[i].score;
		
		if (q[i].label == end || !q[i+1]) break;
		
	}
	
	return score;
}

